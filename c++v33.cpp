#include<iostream>
using namespace std;
int main()
{
    int digit;
    cout<<"Enter a digit:";
    cin>>digit;

    switch(digit)
    {

    case 0:
        cout<<"Zero";
        break;

    case 1:
        cout<<"One";
        break;

    case 2:
        cout<<"two";
        break;

    case 3:
        cout<<"Three";
        break;

    case 4:
        cout<<"Four";
        break;

    case 5:
        cout<<"Five";
        break;

    case 6:
        cout<<"six";
        break;

    case 7:
        cout<<"seven";
        break;

    case 8:
        cout<<"eight";
        break;

    case 9:
        cout<<"nine";
        break;


    }



    return 0;
}
